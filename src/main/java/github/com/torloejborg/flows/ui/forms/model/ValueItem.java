package github.com.torloejborg.flows.ui.forms.model;

public class ValueItem {

    public String label;
    public String value;

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
